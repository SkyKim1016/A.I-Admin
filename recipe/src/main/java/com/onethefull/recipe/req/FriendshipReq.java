package com.onethefull.recipe.req;

import com.onethefull.recipe.comm.req.SearchReq;
import com.onethefull.recipe.type.FriendshipStatusType;

public class FriendshipReq extends SearchReq {
	private String userId;
	private String requesterId;
	private String friendName;
	private FriendshipStatusType status;
	
	public FriendshipReq() {
		super();
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getRequesterId() {
		return requesterId;
	}

	public void setRequesterId(String requesterId) {
		this.requesterId = requesterId;
	}

	public String getFriendName() {
		return friendName;
	}

	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}

	public FriendshipStatusType getStatus() {
		return status;
	}

	public void setStatus(FriendshipStatusType status) {
		this.status = status;
	}

}
