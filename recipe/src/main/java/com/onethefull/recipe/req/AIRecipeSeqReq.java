package com.onethefull.recipe.req;

public class AIRecipeSeqReq {
	String id_recipe;
	int stepSeq;
	
	public AIRecipeSeqReq(){}

	public String getId_recipe() {
		return id_recipe;
	}

	public void setId_recipe(String id_recipe) {
		this.id_recipe = id_recipe;
	}

	public int getStepSeq() {
		return stepSeq;
	}

	public void setStepSeq(int stepSeq) {
		this.stepSeq = stepSeq;
	}

	public AIRecipeSeqReq(String id_recipe, int stepSeq) {
		super();
		this.id_recipe = id_recipe;
		this.stepSeq = stepSeq;
	}

}
