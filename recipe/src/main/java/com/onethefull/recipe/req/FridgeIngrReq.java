package com.onethefull.recipe.req;

import com.onethefull.recipe.type.FridgeIngrType;

public class FridgeIngrReq {
	private String userId;
	private String ingredientId;
	private String ingredientName;
	private String ingredientQnt;
	private String ingredientDday;
	private FridgeIngrType type = FridgeIngrType.INSERT;
	private int resultCode;
	
	public FridgeIngrReq(){}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getIngredientId() {
		return ingredientId;
	}

	public void setIngredientId(String ingredientId) {
		this.ingredientId = ingredientId;
	}

	public String getIngredientName() {
		return ingredientName;
	}

	public void setIngredientName(String ingredientName) {
		this.ingredientName = ingredientName;
	}

	public String getIngredientQnt() {
		return ingredientQnt;
	}

	public void setIngredientQnt(String ingredientQnt) {
		this.ingredientQnt = ingredientQnt;
	}

	public String getIngredientDday() {
		return ingredientDday;
	}

	public void setIngredientDday(String ingredientDday) {
		this.ingredientDday = ingredientDday;
	}

	public FridgeIngrType getType() {
		return type;
	}

	public void setType(FridgeIngrType type) {
		this.type = type;
	}

	public int getResultCode() {
		return resultCode;
	}

	public void setResultCode(int resultCode) {
		this.resultCode = resultCode;
	}
}
