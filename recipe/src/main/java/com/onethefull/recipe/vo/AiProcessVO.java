package com.onethefull.recipe.vo;

public class AiProcessVO {
	String id_recipe, seq, process_info;
	
	public AiProcessVO(){}
	public AiProcessVO(String id_recipe, String seq, String process_info) {
		super();
		this.id_recipe = id_recipe;
		this.seq = seq;
		this.process_info = process_info;
	}

	public String getId_recipe() {
		return id_recipe;
	}

	public void setId_recipe(String id_recipe) {
		this.id_recipe = id_recipe;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	public String getProcess_info() {
		return process_info;
	}

	public void setProcess_info(String process_info) {
		this.process_info = process_info;
	}
}
