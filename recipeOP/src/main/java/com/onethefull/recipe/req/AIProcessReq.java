package com.onethefull.recipe.req;

import com.onethefull.recipe.comm.req.BaseReq;

public class AIProcessReq extends BaseReq {
	String recipeId, ingriName;
	public AIProcessReq(){}
	public AIProcessReq(String recipeId, String ingriName) {
		super();
		this.recipeId = recipeId;
		this.ingriName = ingriName;
	}
	public String getRecipeId() {
		return recipeId;
	}
	public void setRecipeId(String recipeId) {
		this.recipeId = recipeId;
	}
	public String getIngriName() {
		return ingriName;
	}
	public void setIngriName(String ingriName) {
		this.ingriName = ingriName;
	}
}
