package com.onethefull.recipe.vo;

public class UploadVO {
	String path;
	String imageOriginalPath;
	String imageMiddlePath;
	String imageThumbPath;
		
	public UploadVO() {
	}	
	public UploadVO(String path) {
		this.path = path;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getImageOriginalPath() {
		return imageOriginalPath;
	}
	public void setImageOriginalPath(String imageOriginalPath) {
		this.imageOriginalPath = imageOriginalPath;
	}
	public String getImageMiddlePath() {
		return imageMiddlePath;
	}
	public void setImageMiddlePath(String imageMiddlePath) {
		this.imageMiddlePath = imageMiddlePath;
	}
	public String getImageThumbPath() {
		return imageThumbPath;
	}
	public void setImageThumbPath(String imageThumbPath) {
		this.imageThumbPath = imageThumbPath;
	}
}
