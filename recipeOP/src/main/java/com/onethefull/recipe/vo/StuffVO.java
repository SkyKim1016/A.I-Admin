package com.onethefull.recipe.vo;

public class StuffVO {
	private String stuffId;
	private String stuffName;
	private String stuffCategoryId;
	private String stuffCategoryName;
	private String stuffImageUrl;

	public StuffVO() {
	}

	public String getStuffId() {
		return stuffId;
	}

	public void setStuffId(String stuffId) {
		this.stuffId = stuffId;
	}

	public String getStuffName() {
		return stuffName;
	}

	public void setStuffName(String stuffName) {
		this.stuffName = stuffName;
	}

	public String getStuffCategoryId() {
		return stuffCategoryId;
	}

	public void setStuffCategoryId(String stuffCategoryId) {
		this.stuffCategoryId = stuffCategoryId;
	}

	public String getStuffCategoryName() {
		return stuffCategoryName;
	}

	public void setStuffCategoryName(String stuffCategoryName) {
		this.stuffCategoryName = stuffCategoryName;
	}

	public String getStuffImageUrl() {
		return stuffImageUrl;
	}

	public void setStuffImageUrl(String stuffImageUrl) {
		this.stuffImageUrl = stuffImageUrl;
	}
}